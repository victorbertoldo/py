a = 2
b = 6
print('a:', type(a))
print('b:', type(b))

# Os dois são inteiros

c = a / b

print('c:', type(c))

# neste caso o python já faz a conversão automatica,
# pois a divisão de um numero inteiro é um numero racional.
# Por tanto o resultado de uma divisão, mesmo o resultado sendo um
# numero inteiro, ele será convertido para float
