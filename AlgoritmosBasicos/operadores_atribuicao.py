# Operador de atribuição


n = 3
n = n + 2
print(n)

n += 5  # acrescenta 5 ao valor
print(n)

n -= 2  # subtrai o valor
print(n)

n *= 3  # Multiplica o valor
print(n)

n /= 4  # Divide o valor
print(n)

n %= 4  # Módulo ou resto da divisão
print(n)

n **= 2  # Numero elevado a Potencia
print(n)

n //= 3  # Divisão inteira
print(n)
